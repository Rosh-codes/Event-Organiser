export const filterEvents = (events, searchTerm, category) => {
  return events.filter((event) => {
    // Case-insensitive search across multiple fields
    const searchLower = searchTerm ? searchTerm.toLowerCase().trim() : '';
    const matchesSearch = !searchTerm || [
      event.title,
      event.description,
      event.location,
      event.category
    ].some(field => field.toLowerCase().includes(searchLower));

    // Exact category matching
    const matchesCategory = !category || event.category.toLowerCase() === category.toLowerCase();

    return matchesSearch && matchesCategory;
  });
};

export const getFeaturedEvents = (events) => {
  return events.filter((event) => event.featured);
};

export const sortEventsByDate = (events) => {
  return [...events].sort((a, b) => new Date(a.date) - new Date(b.date));
};